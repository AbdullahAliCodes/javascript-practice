
const fruits = ["apple", "banana", 
    "oranges", "pear"];

fruits[0] = "potato"
console.log(fruits[0]);

// removes the LAST element
fruits.pop();
console.log(fruits);

// adds an element to the END of the array
fruits.push("watermelon");
console.log(fruits);


















